    private void populateTable() {
        
        DefaultTableModel model= (DefaultTableModel) tblPerson.getModel();
        model.setRowCount(0);
         for (Person u:medSystem.personDirectory.getdirectory()){
             Object[] row= new Object[7];
             row[0]=u.getId();
             row[1]= u.getName();
             row[2]= u.getAge();
             row[3]= u.getAddress();
             row[4]= u.getCommunity();
             row[5]= u.getCity();  
             if (u.isIsPatient()){
                 Patient patient = (Patient) u;
        EncounterHistory encounterhistory=patient.getEncounterhistory();
        int encountersSize = encounterhistory.getEncounters().size();
        if (encountersSize>0){
        int bp = encounterhistory.getEncounters().get(encountersSize-1).getVitalsign().getBp();
        row[6]= bp;
             }}
             model.addRow(row);             
         }
    }
private void ShowAbnormal() {
        DefaultTableModel model = (DefaultTableModel) tblPerson.getModel();
        model.setRowCount(0);

        String communitysearch = txtcomsearch.getText();
       
        int minage= Integer.parseInt(txtminage.getText());
        int maxage= Integer.parseInt(txtmaxage.getText());
        int bp = 0;    
            for (Person u:medSystem.personDirectory.getdirectory()){
                String comchk = u.getCommunity();
                 int agegroup = u.getAge();
                 if (u.isIsPatient()){
                 Patient patient = (Patient) u;
                 EncounterHistory encounterhistory=patient.getEncounterhistory();
                 int encountersSize = encounterhistory.getEncounters().size();
                  if (encountersSize>0){
                     bp = encounterhistory.getEncounters().get(encountersSize-1).getVitalsign().getBp();
                 
                  }
                  
                
                if (comchk.equals(communitysearch)&& (agegroup >= minage && agegroup<=maxage)&& (bp>140 || bp<100)){
                    
             System.out.print(u.getAge() + "\n");
             Object[] row= new Object[7];
             row[0]=u.getId();
             row[1]= u.getName();
             row[2]= u.getAge();
             row[3]= u.getAddress();
             row[4]= u.getCommunity();
             row[5]= u.getCity(); 
             row[6]= bp;
             
             model.addRow(row);
                 }        
                 }
            }
            Integer rows = model.getRowCount();
            txtnum.setText(rows.toString());
}
