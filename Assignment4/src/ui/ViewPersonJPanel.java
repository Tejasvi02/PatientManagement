    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex= tblPerson.getSelectedRow();
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select row to delete");
            return;
        }
        
        DefaultTableModel model= (DefaultTableModel) tblPerson.getModel();
        int selectedPersonID= (Integer)model.getValueAt(selectedRowIndex, 0);
        var selectedPersons = medSystem.personDirectory.personMap.get(selectedPersonID);
        // Remove the person from directory as well as map
        medSystem.personDirectory.deletePersons(selectedPersons);
        medSystem.personDirectory.personMap.remove(selectedPersonID);      
        
        JOptionPane.showMessageDialog(this, "Selected row deleted");
        populateTable();
        
    }//GEN-LAST:event_btnDeleteActionPerformed

